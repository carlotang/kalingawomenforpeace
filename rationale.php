                that
                we, the researchers, aim to fill. We are of the view that that the reason behind this is that Bodong has
                been dominated by men for centuries, so most of the previous studies were focused on their roles.
                Moreover,
                we are also of the view that less interest is shown not only to the roles of women in Bodong, but to
                Bodong
                in general, because younger Kalinga generations are becoming less and less aware of its processes due to
                their exposure to mainstream justice systems—a phenomenon resulting from globalization and
                migration.
            </p>
