            <p class="content">In every Kalinga occasion, food plays a vital role in making the moment as worthwhile and
                meaningful as possible. Food has always been an integral part of Kalinga culture, reflected in the
                variety of dishes served during important events. One such occasion where the Kalingas showcase their
                rich culinary tradition is the culmination of a successful establishment of the <i>Bodong</i>, a peace
                pact
                formalized after a long and meticulous negotiation. </p>

            <p class="content">Typically, a <i>Bodong</i> is formally celebrated with a ceremonial feast following its
                successful negotiation. Although this feast traditionally takes place immediately afterward, unavoidable
                circumstances may delay the formal celebration for several months or even years. Regardless of the
                timing, the <i>Bodong</i> celebration carries great importance and anticipation. Not only does it mark
                the
                peaceful co-existence between tribes, but it also underscores their shared commitment to mutual
                socio-economic development.
            </p>

            <p class="content">The celebration is characterized by festivity, with the host tribe warmly entertaining
                the visiting tribe. The visiting tribe, in turn, actively participates in dancing and singing. In recent
                times, teachers, pupils, and students have also joined in by performing choruses and folk dances. As
                part of the event, members of both tribes share their experiences and reflections related to the
                <i>Bodong</i>,
                further fostering a sense of unity and understanding.

            </p>

            <p class="content">To make the occasion truly memorable, food and drinks are served generously. But beyond
                merely satisfying hunger and thirst, food and beverages play a deeper role. It is a long-held belief
                among the Kalingas that harming someone after giving them food or drink is taboo, highlighting the
                protective and sacred role that hospitality plays in these gatherings.

            </p>

            <p class="content">Delicious Kalinga foods are predominantly prepared by women, who diligently ensure only
                the finest dishes are offered to their guests. For many, this responsibility underscores the vital role
                women play in strengthening bonds between tribes through their culinary contributions.

            </p>

            <p class="content">One iconic delicacy often served during these events is <i>daykot</i>, made of powdered
                glutinous rice molded into cakes and coated with coconut extract, known as <i>ladok</i> or <i>larag</i>.
                For the
                Kalingas, sticky rice symbolizes the aspiration of tribes and clans to grow closer and strengthen their
                connection.


            </p>

            <p class="content">A variation of <i>daykot</i> is the popular <i>inandila</i>. This tongue-shaped sticky
                rice cake is
                paired with the rich and flavorful Kalinga coffee. It is topped on both sides with latik (coconut
                caramel) and grated nuts, earning it the affectionate nickname “back-to-back.”

            </p>

            <p class="content">For the main course, carabaos are typically slaughtered to prepare the finest meat for
                the feast. Women from the host tribe cook the carabao meat in a simmering broth to warm and nourish
                everyone present. Serving less desirable meats, such as pork, is considered unusual, as it could imply
                that the guests are not valued. Among Kalingas, carabao meat is regarded as the premium delicacy,
                reserved for the most honored occasions.


            </p>

            <p class="content">To complement the meal, attendees enjoy sugarcane wine, locally called <i>basi</i>.
                Drinking
                basi is often central to the <i>Bodong</i> celebration, marked by a unique ritual known as
                <i>tumangad</i>,
                which
                means "looking up." Various porcelain containers, called tapac and payao, are filled with basi and
                placed on upside-down mortars. Participants take turns sipping from the shared containers, looking up to
                the sky afterward in silent acknowledgment, invoking the mighty Kaboniyan as a witness to the newly
                concluded peace pact.


            </p>

            <p class="content">Indeed, these traditional Kalinga dishes and drinks not only highlight a rich culinary
