            <p class="content">The exponential growth of numerous innovations and developments has convinced many that
                the
                seed of values and culture indigenous peoples have nourished for generations is slowly dying. The influx
                of
                external influence and mobilization of local folks to pursue education or work is believed to cause this
                phenomenon.

            </p>

            <p class="content">With this, young members of the
                indigenous peoples are called to preserve the values and culture of their ethnic group, because they are
                on
                the verge of being forgotten. The youth is urged to continuously maintain and practice the cultural
                legacy
                they inherited from their ancestors. In their respective regions, indigenous peoples have their own
                system
                of law enforcement and peace maintenance. One great example of this is the Kalinga communities’
                peace-oriented practice of Bodong.


            </p>

            <p class="content">The young generation of the Kalinga society believes that Bodong is a system of rules
                that
                brings about harmony and peace between two tribes, by letting them reach a bilateral agreement.
                According to
                a young male Kalinga folk, neighboring provinces like Abra also practice it. This substantiates the
                significant role of Bodong in Kalinga and nearby lands.
            </p>

            <p class="content">Interestingly, 2 out of 7 youths, from various tribes who were interviewed, already
                participated in the Bodong Process. One male participant admitted that he has seen a Bodong Process
                twice,
                in 2016 and 2018. “It is a common practice. We witness it because it is usually practiced,” he added. A
                female Kalinga community member also had the experience of attending a Bodong process when she was
                younger.
                According to her, peace pact holders must be qualified to lead the tribe in a peaceful and harmonious
                way.
                These leaders are usually men, but the majority of young Kalinga women believe that they can also be
                Bodong
                holders nowadays.
            </p>

            <p class="content">Similar to the indigenous customs and traditions that are handed down through word of
                mouth,
                Bodong is passed from generation to generation. That’s how the youth of Kalinga get to understand the
                Bodong
                process they inherited from the past. A male interviewee shared that he holds adequate knowledge of the
                Bodong process. A Pangat in their community, his grandfather informally educated him on the procedure.
                He
                later learned that the Bodong system of Kalinga is similar in every tribe.
            </p>

            <p class="content">Perceived by youth to still have relevance in this day and age, Bodong remains to be
                widely
                practiced and greatly valued in Kalinga communities. Upheld as a sacred covenant, it fosters cooperation
                between tribes and promotes the peaceful settlement of disputes. Another young female Kalinga, who is
                taking
                undergraduate study at the faculty of law, expressed, "I am a supporter of Bodong, even though I am
                studying
                law, for my perception at the moment I still believe the Bodong process and its effectiveness."
            </p>

            <p class="content">The Bodong serves as an instrument to serve justice and achieve a more culture-oriented
                legal
                process, as it is a simple, quick, and effective settlement for tribal conflicts. Hence, by all
                accounts,
                the process will continue to be observed and respected. “It is relevant and it is part of our history,”
                a
                Kalinga woman remarked. For Bodong to remain alive in the future, seeds of cultural knowledge must be
                planted and cultivated in the minds of the young generation.
            </p>

            <p class="content">The Kalinga youth, who were asked if they want to get themselves involved in the process,
                signified their interest. However, not all of them, the male interviewees specifically, wanted to be
                Bodong
                holders because of the huge responsibility attached to it. But they’re not totally closing the door if
                the
                community chooses them. Confident of their abilities, some female Kalinga youth conveyed their
                willingness
                to be peace pact holders and take an active role in other capacities.
            </p>

            <p class="content">The youth’s fervor on preserving their traditional practices and identity, especially the
                Bodong, brings hope to their indigenous communities. It ensures that their Kalinga values and culture
                are in
                safe hands because young and passionate people like them are there to nourish it.
