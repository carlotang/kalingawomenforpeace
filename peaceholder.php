                woman at
                any time. In fact, it is believed that harming a woman will bring ruin and curse to the tribe of the
                wrongdoer.
            </p>

            <p class="content">It is, therefore, not a surprise that in most <i>pagtas</i>, a chapter is specifically
                devoted for
                provisions that make certain acts against women criminal. The commission of these acts renders the
                perpetrator liable for penalties based on the gravity of the offense.
            </p>

            <p class="content">Among the acts considered as criminal is <i>pugod/gabao</i> (rape). The person who
                commits
                this act
                may be held to answer by paying the victim twelve carabaos. One author noted that since this act is a
                taboo
                and the penalty is rather high, this crime is seldom committed in Kalinga villages even up to this day.
            </p>

            <p class="content">Another act that is considered a crime under a pagta is the <i>sowao/daldag</i>. This act
                pertains
                to an illicit affair that a married man incurs with the wife of another man. Usually, the guilty man is
                demanded be fined with one carabao, which is to be butchered during a settlement. The guilty man may
                also be
                required to pay a fine of three carabaos to be delivered to the husband of the woman. The guilty woman
                may
                also be held to answer for two carabaos to be delivered to the wife of the offending husband.
            </p>

            <p class="content">Acts of lasciviousness, permanent abandonment of a spouse, physical injuries,
                <i>lukluk/lobak</i>
                (desecrating a widow), and <i>toddak di babae</i> (abduction), are some of the other acts punishable by
                the
                <i>pagta</i>.
                It must be noted that a woman may also be held liable under the <i>pagta</i> if she commits some of the
                acts
                referred above.
            </p>

            <p class="content">However, more than protecting women by making the above acts committed against them
                criminal,
                most <i>pagtas</i> now accord to Kalinga women a status that was once inexistent, particularly in the
                practice of
                Bodong. Today, Kalinga women’s roles are recognized by the pagta as indispensable in the achievement of
                peace and development in the Kalinga society; hence, Kalinga women are encouraged to be <i>peace pact
                    holders</i>
                and to actively participate in all <i>Bodong</i> undertakings. This does not only protect Kalinga women,
                but
                also
